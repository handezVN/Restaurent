package DTO;

/**
 *
 * @author handez
 */
public class ProductDTO {
    private String name;
    private String product_id;
    private int price;

    public ProductDTO(String name, String product_id, int price) {
        this.name = name;
        this.product_id = product_id;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getProduct_id() {
        return product_id;
    }

    public void setProduct_id(String product_id) {
        this.product_id = product_id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
